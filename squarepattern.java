import java.util.*;

public class squarepattern {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);
        // for loop
        for (int line = 0; line < 4; line++) {
            System.out.println("****");
        }

        System.out.println();

        // while loop
        int i = 0;
        while (i < 4) {
            System.out.println("****");
            i++;
        }

    }
}